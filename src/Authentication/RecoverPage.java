    private void btnSubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSubmitActionPerformed
        username = tfUsername.getText();
        question = (String) cbQuestion.getSelectedItem();
        answer = tfAnswer.getText();
        password = pfPassword.getText();
        confirmPassword = pfConfirmPassword.getText();

        // Validate
        if (username.isBlank() || answer.isBlank() || password.isBlank() || confirmPassword.isBlank()) {
            JOptionPane.showMessageDialog(this, "Please enter all fields");
            return;
        }

        if (!password.equals(confirmPassword)) {
            JOptionPane.showMessageDialog(this, "Password does not match");
            return;
        }

        try {
            Connection conn = DriverManager.getConnection(DB_URL, USERNAME, PASSWORD);
            // connect to database
            
            String sqlUsername = "select Username from ACCOUNT where Username = ?";
            PreparedStatement preparedStatementUsername = conn.prepareStatement(sqlUsername);
            preparedStatementUsername.setString(1, username);

            ResultSet resultSetUsername = preparedStatementUsername.executeQuery();

            // Checks if credentials exists
            if (resultSetUsername.next()) {
                String sqlPassword = "select * from FORGOT_PASSWORD where SecurityQuestion = ? and Answer = ?";
                PreparedStatement preparedStatementPassword = conn.prepareStatement(sqlPassword);
                preparedStatementPassword.setString(1, question);
                preparedStatementPassword.setString(2, answer);

                // If exists and correct, proceed to update
                ResultSet resultSetPassword = preparedStatementPassword.executeQuery();
                if (resultSetPassword.next()) {
                    userId = resultSetPassword.getInt("UserID");

                    String sqlNewPassword = "update ACCOUNT set UserPassword = ? where UserID = ?";
                    PreparedStatement preparedStatementNewPassword = conn.prepareStatement(sqlNewPassword);
                    preparedStatementNewPassword.setString(1, password);
                    preparedStatementNewPassword.setInt(2, userId);
                    
                    int updatedRow = preparedStatementNewPassword.executeUpdate();
                    if(updatedRow > 0){
                        JOptionPane.showMessageDialog(this, "Successfully updated password");
                        
                        // Get the current date and time
                        LocalDateTime now = LocalDateTime.now();
                        // Convert LocalDateTime to java.sql.Timestamp
                        Timestamp currentTimestamp = Timestamp.valueOf(now);
                        
                        String sqlInsertLog = "insert into USER_LOG (UserID, UserAction, ActionDate) values (?,?,?)";
                        PreparedStatement preparedStatementInsertLog= conn.prepareStatement(sqlInsertLog);
                        preparedStatementInsertLog.setInt(1, userId);
                        preparedStatementInsertLog.setString(2, "Updated password");
                        preparedStatementInsertLog.setTimestamp(3, currentTimestamp);
                        
                        int insertedRow = preparedStatementInsertLog.executeUpdate();
                        if(insertedRow > 0){
                            System.out.println("User log updated");
                        }
                    }
                }else{
                    JOptionPane.showMessageDialog(this, "Answer is wrong");
                }
            } else {
                JOptionPane.showMessageDialog(this, "Username not found");
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e);
        }
    }//GEN-LAST:event_btnSubmitActionPerformed

